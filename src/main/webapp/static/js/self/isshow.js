$(function(){
	//返回按钮的功能操作
  function _backBtn(){
    //分别点击左侧导航，默认进入第一个界面
    $(".config_nav").on("click","h2",function(){
      if($(this).text()=="基础配置"){
        $(".brand_list").show();
        $(".product_list").hide();
        $(".category_list").hide();
        $(".category_allocation").hide();
        $(".design_list").hide();
        $(".glass_list").hide();
        $(".model_list").hide();
        $(".Composite_list").hide();
        $(".acting_list").hide();
        $(".model_config").hide();
      }else if($(this).text()=="关联配置"){
        $(".config_list").show();
        $(".relevancy_config").hide();
        $(".series_list").hide();
        $(".contract_linehandware").hide();
      }else if($(this).text()=="供应配置"){
        $(".supply_list").show();
        $(".supplyrelevancy_config").hide();
        $(".supplyseries_list").hide();
        $(".supplycontract_linehandware").hide();
      }else if($(this).text()=="销售配置"){
        $(".sell_list").show();
        $(".sellrelevancy_config").hide();
        $(".sellseries_list").hide();
        $(".sellcontract_linehandware").hide();
      }
    })
    //点击产品列表的返回按钮，进入品牌列表部分
    $("#brand_list_btn").on("click",function(){
      $(".brand_list").show();
      $(".product_list").hide();
    })
    //点击品类列表的返回按钮，进入产品列表部分
    $("#product_list_btn").on("click",function(){
      $(".product_list").show();
      $(".category_area").hide();
    })
    //点击套线列表的返回按钮，进入产品列表部分
    $("#line_list_btn").on("click",function(){
      $(".product_list").show();
      $(".line_area").hide();
    })
    //点击材质列表的返回按钮，进入产品列表部分
    $("#materail_list_btn").on("click",function(){
      $(".product_list").show();
      $(".materail_area").hide();
    })
    //点击五金列表的返回按钮，进入产品列表部分
    $("#hardware_list_btn").on("click",function(){
      $(".product_list").show();
      $(".hardware_area").hide();
    })
    //点击品类配置列表的返回按钮，进入品类列表部分
    $("#category_allocation_btn").on("click",function(){
      $(".category_list").show();
      $(".category_allocation").hide();
    })
    //点击尺寸配置的返回按钮，进入品类配置列表部分
    $("#Size_list_btn").on("click",function(){
      $(".category_allocation").show();
      $(".Size_list").hide();
    })
    //点击不同品类门的型号列表的返回按钮，进入品类配置列表部分
    $("#model_list_btn").on("click",function(){
      $(".category_allocation").show();
      $(".model_list").hide();
    })
    //点击推拉门数量配置操作的返回按钮，进入品类配置列表部分
    $("#acting_list_btn").on("click",function(){
      $(".category_allocation").show();
      $(".acting_list").hide();
    })
    //点击子母门数量配置操作的返回按钮，进入品类配置列表部分
    $("#Composite_list_btn").on("click",function(){
      $(".category_allocation").show();
      $(".Composite_list").hide();
    })
    //点击五金配置列表的返回按钮，进入品类配置列表部分
    $("#glass_list_btn").on("click",function(){
      $(".hardware_area").show();
      $(".glass_list").hide();
    })
    //点击花色配置列表的返回按钮，进入材质列表部分
    $("#design_list_btn").on("click",function(){
      $(".materail_area").show();
      $(".design_list").hide();
    })
  }
   _backBtn();
  //是否显示按钮
  function isShow_btn(){
     //一打开页面没有任何按钮
     $("#newly_btn").hide();
     $(".confirm_btn").hide();
     //当进入尺寸配置界面时，确认提交按钮显示
     $(".Size").on("click",function(){
        $(".confirm_btn").show();
     })
     //当点击型号时，进入型号列表，新增按钮出现
     $(".Model").on("click",function(){
        $("#newly_btn").show();
     })
     //当点击新增按钮时，进入型号配置页面，确认提交按钮出现
     $("#newly_btn").on("click",function(){
        $(".confirm_btn").show();
        $(".confirm_btn").css({"margin-left":"65%"});
     })
     //点击推拉门数量时，进入门扇数量配置页面，确认提交按钮出现
     $("#last_number").on("click",function(){
        $(".confirm_btn").show();
     })
     //点击套线时，进入套线列表，新增按钮出现
     $(".Line").on("click",function(){
        $("#newly_btn").show();
        $("#line_list_btn").show();
     })
     //点击材质列表的每一个系列，进入花色配置列表，新增按钮出现
     $(".picture_materail").on("click","dl",function(){
        $("#newly_btn").show();
     })
     //点击五金列表的任何一个，进入五金的配置列表，新增按钮出现
     $(".picture_hardware").on("click","dl",function(){
        $("#newly_btn").show();
     })
     //点击关联配置中的型号-配置进入型号列表，确认提交按钮替换为下一步按钮
     $(".picture_config").on("click","dl",function(){
        $(".confirm_btn").show();
        $(".confirm_btn").val("下一步");
     })
     //当点击关联配置中的型号列表时，进入系列列表，下一步按钮出现
     $(".model").on("click","dl",function(){
        $(".confirm_btn").show();
        $(".confirm_btn").val("下一步");
        $(".confirm_btn").css({"margin-right":"8%"});
        $(".templte_config").hide();
     })
     //点击销售配置导航，进入区域选择，下一步按钮出现
     $(".sell_nav").on("click",function(){
        $(".confirm_btn").show();
        $(".confirm_btn").val("下一步");
     })
     //点击材质，进入材质列表，新增按钮显示
     $(".material").on("click",function(){
        $("#newly_btn").show();
     })
  }
  isShow_btn();
  //设置不同品类的门对应的型号列表的标题
  function _modelTitle(){
    $("#picture_category").on("click","dl",function(){
      var categoryTitle=$(this).find("dd").text();
      $(".model_title").text(categoryTitle+"型号列表");
    })
  }
  _modelTitle();
  //型号配置关闭按钮的操作功能
  function _closeModelconfig(){
    $(".Model").on("click",function(){
      if($(".model_title").text()=="平开门型号列表" || $(".model_title").text()=="内置推拉门型号列表" || $(".model_title").text()=="外挂推拉门型号列表" || $(".model_title").text()=="对开门型号列表" || $(".model_title").text()=="子母门型号列表" || $(".model_title").text()=="窗套/垭口型号列表" || $(".model_title").text()=="护角型号列表"){
        $(".close_btn").attr("id","side-hingedClose");
        $("#side-hingedClose").on("click",function(){
          $(".model_config").hide();
          $(".model_list").show();
          $(".confirm_btn").hide();  //确认提交按钮隐藏
          $(".line_area").hide();
        })
      }
    })
    $(".Line").on("click",function(){
      $(".close_btn").attr("id","LineClose");
      $("#LineClose").on("click",function(){
        $(".model_config").hide();
        $(".line_area").show();
        $(".confirm_btn").hide();  //确认提交按钮隐藏
        $(".model_list").hide();
      })
    })
    $(".material").on("click",function(){
      $(".close_btn").attr("id","materialClose");
      $("#materialClose").on("click",function(){
        $(".model_config").hide();
        $(".materail_area").show();
        $(".confirm_btn").hide();  //确认提交按钮隐藏
        $(".model_list").hide();
      })
    })
    $(".picture_design").on("click","dl",function(){
      $(".close_btn").attr("id","colorClose");
      $("#colorClose").on("click",function(){
        $(".model_config").hide();
        $(".confirm_btn").hide();  //确认提交按钮隐藏
        $(".glass_list").hide();
        $(".design_list").show();
        $(".materail_area").hide();
      })
    })
    $("#picture_hardware").on("click","dl",function(){
      $(".close_btn").attr("id","hardwareClose");
      $("#hardwareClose").on("click",function(){
        $(".model_config").hide();
        $(".glass_list").show();
        $(".confirm_btn").hide();  //确认提交按钮隐藏
        $(".design_list").hide();
        $(".model_list").hide();
      })
    })
  }
  _closeModelconfig();
  //尺寸配置录入界面，点击返回按钮，清空表单里面的所有数据
  $(".back_btn").on("click",function(){
    $(".area_list").find("input[type=text]").val("");
    $(".area_list").find("input[type=text]").removeAttr("disabled","disabled");
    $(".confirm_btn").removeAttr('disabled','disabled');
  })
})