$(function(){
    //品牌、品类、型号的展开，折叠
    var leftStyleFn=function(){
        $(".brand_box h2").find("img").attr("src","static/images/2_03.png");
        $(".click_style").on("click",function(){
           if($(this).next().is(":hidden")){
              $(this).next().slideDown();
              $(this).find("img").attr("src","static/images/2_03.png");
           }else{
              $(this).next().slideUp();
              $(this).find("img").attr("src","static/images/1_03.png");
           }
        })
    }
    leftStyleFn();
    //尺寸，材质，配置的切换
    $("#tab_change").find("li").on("click",function(){
       var index=$(this).index();
       $(this).addClass("active_style").siblings().removeClass("active_style");
       $(".content_area").find(".area").eq(index).show().siblings().hide();
    })
    //窗口调节大小时，右边盒子外观部分高度的计算
    function HeightFn(){
       var _liW=$("._ulW").find("li").width();
           $("._ulW").find("li").css({"height":_liW*3/4+"px"});
           $("._ulW").find("li").css({"line-height":_liW*3/4+"px"});
           $("._ulW").css({"height":_liW*3/4+"px"});
    }
    $("window").on("resize",function(){
       HeightFn();
    })
    HeightFn();
    //高度调整
    $('.single-sliderH').jRange({
      from:600,
      to:1200,
      step:5,
      format:'%s',
      showLabels:true,
      showScale:true
    }) 
    //宽度调整
    $('.single-sliderW').jRange({
      from:600,
      to:1200,
      step:5,
      format:'%s',
      showLabels:true,
      showScale:true
    })
    //深度调整
    $('.single-sliderD').jRange({
      from:600,
      to:1200,
      step:5,
      format:'%s',
      showLabels:true,
      showScale:true
    })
    //是否显示隐藏
    var isSliderFn=function(){
        //柜体切角的展开折叠
        $("#Doorlengthen").on("click",function(){
            if($("#Doorlengthen_input").is(":checked")){
              $("#doorMark").show();
            }else{
              $("#doorMark").hide();
            }
        })
        //修改纹理方向的展开折叠
        $(".choice").on("click",function(){
            if($("#choice_texture").is(":checked")){
              $(".texture_decoration").show();
            }else{
              $(".texture_decoration").hide();
            }
        })
        //点击门板系列，进入二级页面(门板花色，造型，纹理)
        $(".flowser_material").on("click","dl",function(){
           $(this).addClass('active').siblings().removeClass('active');
           $(".flowser_show").show();
           $("#underline").show();
        })
        //配置列表的柜上添加的水槽和龙头的二级页面的显示隐藏
        $("#double").on("click",function(){
            if($("#double_input").is(":checked")){
              $(".mantle_box").show();
            }else{
              $(".mantle_box").hide();
            }
        })
        //点击电器格区的电器选择，进入二级型号选择页面
        $(".electrica_list").on("click","dl",function(){
            $(this).addClass('active').siblings().removeClass('active');
            $(".electrica_Model").show();
        })
        //配置界面的客户自购水槽
        $(".Purchase_passenger").on("click",function(){
            if($("#Purchase_waterInput").is(":checked")){
                $(".Purchase_water").show();
            }else{
                $(".Purchase_water").hide();
            }
        })
        //配置界面的客户自购龙头
        $(".Purchase_tapPassenger").on("click",function(){
            if($("#Purchase_tapInput").is(":checked")){
                $(".Purchase_tap").show();
            }else{
                $(".Purchase_tap").hide();
            }
        })
        //点击添加灶具，二级页面的显示
        $("#add_cooking").on("click",function(){
            if($("#add_cookingInput").is(":checked")){
                $(".cooking_box").show();
            }else{
                $(".cooking_box").hide();
            }
        })
        //配置界面的客户自购灶具
        $(".Purchase_cookingName").on("click",function(){
            if($("#Purchase_cookingInput").is(":checked")){
                $(".Purchase_cooking").show();
            }else{
                $(".Purchase_cooking").hide();
            }
        })
    }
    isSliderFn();
})
